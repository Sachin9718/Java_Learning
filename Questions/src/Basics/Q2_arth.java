package Basics;
public class Q2_arth {
    // adding
    public int add(int x, int y) {
        return x+y;
    }

    // subtracting
    public int sub(int x, int y) {
        return x-y;
    }

    // multiplication
    public int mult(int x, int y) {
        return x*y;
    }

    // division
    public int div(int x, int y) {
        return x/y;
    }

    // modulus
    public int mod(int x, int y){
        return x/y;
    }
}
